# Initialize colorama
colorama.init(autoreset=True)

# Function to print colored text
def write(text, color):
    match color:
        case "red":
            print(colorama.Fore.RED + text)
        case "green":
            print(colorama.Fore.GREEN + text)
        case "yellow":
            print(colorama.Fore.YELLOW + text)
        case _:
            print(text)